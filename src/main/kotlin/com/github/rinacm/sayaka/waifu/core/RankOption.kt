enum class RankOption(val alias: String) {
    /**
     * Daily ranking
     */
    DAY("day"),

    /**
     * Weekly ranking
     */
    WEEK("week"),

    /**
     * Monthly ranking
     */
    MONTH("month"),

    /**
     * Daily ranking for male
     */
    DAY_MALE("day_male"),

    /**
     * Daily ranking for female
     */
    DAY_FEMALE("day_female"),

    /**
     * Daily ranking for original works
     */
    WEEK_ORIGINAL("week_original"),

    /**
     * Daily ranking for newbies
     */
    WEEK_ROOKIE("week_rookie")
}